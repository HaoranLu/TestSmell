package it.unisa.scam14.beans;

public class Blob {

	private String name;
	private boolean isBlob;
	private int loc;
	private int lcom;
	private int wmc;
	private int rfc;
	private int cbo;
	private int nom;
	private int noa;
	private int dit;
	private int noc;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isBlob() {
		return isBlob;
	}
	public void setBlob(boolean isBlob) {
		this.isBlob = isBlob;
	}
	public int getLoc() {
		return loc;
	}
	public void setLoc(int loc) {
		this.loc = loc;
	}
	public int getLcom() {
		return lcom;
	}
	public void setLcom(int lcom) {
		this.lcom = lcom;
	}
	public int getWmc() {
		return wmc;
	}
	public void setWmc(int wmc) {
		this.wmc = wmc;
	}
	public int getRfc() {
		return rfc;
	}
	public void setRfc(int rfc) {
		this.rfc = rfc;
	}
	public int getCbo() {
		return cbo;
	}
	public void setCbo(int cbo) {
		this.cbo = cbo;
	}
	public int getNom() {
		return nom;
	}
	public void setNom(int nom) {
		this.nom = nom;
	}
	public int getNoa() {
		return noa;
	}
	public void setNoa(int noa) {
		this.noa = noa;
	}
	public int getDit() {
		return dit;
	}
	public void setDit(int dit) {
		this.dit = dit;
	}
	public int getNoc() {
		return noc;
	}
	public void setNoc(int noc) {
		this.noc = noc;
	}
}
